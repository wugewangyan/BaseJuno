package com.juno.createcode;


public class CreatePagesVO {
	private String createId;
	private String value;
	private String type; 
	private String fieldTitle;
	private String showType;
	private String showTypeValue;
	
	public String getCreateId() {
		return createId;
	}
	public void setCreateId(String createId) {
		this.createId = createId;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getFieldTitle() {
		return fieldTitle;
	}
	public void setFieldTitle(String fieldTitle) {
		this.fieldTitle = fieldTitle;
	}
	public String getShowType() {
		return showType;
	}
	public void setShowType(String showType) {
		this.showType = showType;
	}
	public String getShowTypeValue() {
		return showTypeValue;
	}
	public void setShowTypeValue(String showTypeValue) {
		this.showTypeValue = showTypeValue;
	}
}
