package com.juno.model;



public class Custom {
	
	private String customId;
    private String customName;
    private String customTelphone;
    private String customDesc;
    
	public String getCustomId() {
		return customId;
	}
	public void setCustomId(String customId) {
		this.customId = customId;
	}
	public String getCustomName() {
		return customName;
	}
	public void setCustomName(String customName) {
		this.customName = customName;
	}
	public String getCustomTelphone() {
		return customTelphone;
	}
	public void setCustomTelphone(String customTelphone) {
		this.customTelphone = customTelphone;
	}
	public String getCustomDesc() {
		return customDesc;
	}
	public void setCustomDesc(String customDesc) {
		this.customDesc = customDesc;
	}
}
